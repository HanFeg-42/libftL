#include "libft.h"

int ft_isalnum(int c)
{
    //if ((c < 91 && c > 64) || (c > 96 && c < 123) || (c < 58 && c > 47))
    if (ft_isalpha(c) || ft_isdigit(c))   
        return (1);
    return (0);
}
